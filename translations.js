function applyTranslations(lang) {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang] && translations[lang][key] !== undefined) {
      el.textContent = translations[lang][key];
    }
  });

  // Update placeholders
  const placeholders = {
    en: { name: 'Jane Smith', company: 'Acme Corp', email: 'jane@acme.com', message: 'Tell us about your project or challenge...' },
    ro: { name: 'Ion Popescu', company: 'Firma SA', email: 'ion@firma.ro', message: 'Spune-ne despre proiectul sau provocarea ta...' }
  };
  const p = placeholders[lang];
  document.getElementById('name').placeholder = p.name;
  document.getElementById('company').placeholder = p.company;
  document.getElementById('email').placeholder = p.email;
  document.getElementById('message').placeholder = p.message;

  // Update html lang attribute
  document.documentElement.lang = lang;

  // Update lang toggle labels
  const isEN = lang === 'en';
  document.getElementById('langLabel').textContent = isEN ? 'EN' : 'RO';
  document.getElementById('langOther').textContent = isEN ? 'RO' : 'EN';
  document.getElementById('langLabelMobile').textContent = isEN ? 'EN' : 'RO';

  localStorage.setItem('pvs-lang', lang);
}

function toggleLang() {
  const current = localStorage.getItem('pvs-lang') || 'en';
  applyTranslations(current === 'en' ? 'ro' : 'en');
}

// Apply saved or default language on load
(function () {
  const saved = localStorage.getItem('pvs-lang') || 'en';
  if (saved !== 'en') applyTranslations(saved);
})();
